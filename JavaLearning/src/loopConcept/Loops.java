package loopConcept;

public class Loops {

	public static void main(String[] args) 
	{
		int a = 100;
		int b = 0;
		for(int i=0; i<=a; i++)
		{
			System.out.println("The numbers from 0 to 100 are: " +i);
		}
		for(int j=100; j>=b ; j--)
		{
			System.out.println("Numbers in reverse order are: " +j);
		}
	}

}
